2.2 BOOLEANS

The boolean type has two values, false and true, which represent the traditional
boolean values. However, boolens do not hold a monopoly of condition values in lua. 
Any value may represent a condition. Conditioanls (such as the ones in control structures)
consider both false and nil as fals and everything else as true. BEWARE that, unlike 
some other langs, lua considers both 0 and the empty string as true conditions
]]