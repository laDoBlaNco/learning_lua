-- difference_between_data_types.lua

-- for numbers you can perform artithmetic operations directly
num1 = 10
num2 = 5
sum = num1 + num2
print()
print('Sum of numbers:',sum)

-- strings can be concatenated using the '..' operator
firstName = 'Lua'
lastName = 'Developer'
fullName = firstName .. ' ' .. lastName
print()
print('Full Name:',fullName)

-- Bools are commonly used in conditional statements and  logical operators
isReady = true
isComplete = false
print()
print('Is Ready:',isReady)
print('Is Complete:',isComplete)


