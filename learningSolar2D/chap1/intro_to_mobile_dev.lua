MOBILE OPERATING SYSTEMS
The smartphone and table industry is divided by the operating systems (OS) that
runs on the device. An operatoring system handles all of the directions from the
apps that are running and what the user is tapping on as well as connecting to
the internet, handling text messages and phone calls. It is a busy system! As 
I begin to develop applications for smartphones and tables, its importantisimo
that I keep inmind the devices that my apps will be running on. Below I have
the four most popular OSs for mobile devices:

  • Android: The Android OS was developed by Google. The first beta was released
    in 2007 and it has been regularly updated every few months. At the time of 
    this writing Android 16 is the current stable release.

    Native development for Android devices is done with Google's APK (Android
    Programming Kit). But many tools (including Solar2D) also allow me to build
    Android applications.

    The Android OS is available on smartphones and tablets. It is the foundation
    of Amazon's kindle Fire, Google Pixel, and Samsung Galaxy to name a few. 
    Android enjoys a devoted following. Android Apps can be sold on Amazon and
    Google Play.

  • Apple iOS: Apple's iOS waas first released in 2007 as well. Apple iOS can
    only run on Apple hardware according to the licensing agreement that I
    have to sign when I download the software.

    Apple iOS runs on the iPhone and iPad. Currently, Apple iOS apps can only
    be sold through the iTunes and Apple App store.

    Apple iOS native app development is done with xCode. However, like Android
    there are many tools, such as Solar2D that with allow me to build for
    multiple operating systems/devices at the same time.

  • Desktop: Solar2D now allows me to build for Windows and Mac desktops as 
    well. While there are a few minor differences that are required in programming,
    I'll find that creating a desktop game for Windows or mac is as easy as 
    creating a game for mobile.

  • HTML5: Publishing to desktop or mobile devices is a great way to create
    innovative apps, but I'll also have the freedom to publish to web with
    the HTML5 build option in Solar2D

